/*
    Types of productivity objects:
    habits: the simplest type of productivity object, a habit is a task that is done on a regular basis, this will help boost your wellbeing which will be a factor in the quality of work criteria
    projects: a very verstile type of productivity object that represents a temporary module with a set period of time
        this productivity object is filled with both individual tasks and collaboration tasks
        tasks are encapsulated in milestones with deadlines
    goals: a very abstract type of productivity object that represnts interest or vision that is broken down into smaller projects (each goal will be set as long term projects)
        goals will be based on a skill tree (work in progress)
    challenge: a type of productivity object that is time sensitive and temporary with 1 or more people (this type of object will have deadlines and milestones defined by the system)
*/

/*
    Built in productivity objects:
    1. daily routines (morning and evening): a special type of habit project that is done on a daily basis
    2. personal profile: a place to reflect on everything, share your progress
*/
